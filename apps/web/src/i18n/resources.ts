export const LANGUAGE_STORAGE_KEY = 'skynet-language';

export const SUPPORTED_LANGUAGES = ['zh', 'en'] as const;

export type SupportedLanguage = (typeof SUPPORTED_LANGUAGES)[number];

export function normalizeLanguage(value?: string | null): SupportedLanguage | null {
  if (!value) return null;
  const candidates = value
    .split(',')
    .map((candidate) => candidate.trim().split(';')[0]?.toLowerCase())
    .filter(Boolean);

  for (const normalized of candidates) {
    if (normalized.startsWith('zh')) return 'zh';
    if (normalized.startsWith('en')) return 'en';
  }

  return null;
}

export function languageToHtmlLang(language: SupportedLanguage): string {
  return language === 'zh' ? 'zh-CN' : 'en';
}

